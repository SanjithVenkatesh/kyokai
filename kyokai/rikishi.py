class Rikishi:
    def __init__(self, shikona, origin, beya, birthDate, height, weight, rank):
        self.shikona = shikona
        self.origin = origin
        self.beya = beya
        self.birthDate = birthDate
        self.height = height
        self.weight = weight
        self.rank = rank
    
    def __init__(self, shikona):
        self.shikona = shikona
        

    def getShikona(self):
        return self.shikona